    private static Point mercatorProjection(double lat, double lon) {
        double x = EARTH_RADIUS * Math.toRadians(lon);
        double y = EARTH_RADIUS * Math.log(Math.tan(Math.PI / 4 + Math.toRadians(lat) / 2));
        return new Point((int) x, (int) y);
    }

    private static double calculatePolygonArea(List<Point> points) {
        int n = points.size();
        double area = 0.0;

        for (int i = 0; i < n; i++) {
            Point p1 = points.get(i);
            Point p2 = points.get((i + 1) % n);
            area += (p1.x * p2.y) - (p2.x * p1.y);
        }

        return Math.abs(area) / 2.0;
    }

